function renderTabs(container) {
  container.replaceChildren(
    ...categories.map((cat, i) => {
      const isActive = cat.id === activeCategory;
      const btn = el(
        "button",
        {
          class: `tab ${isActive ? "tab-active" : ""}`,
          type: "button",
          "data-cat": cat.id,
          style: `animation-delay:${i * 80}ms`,
          onclick: () => setCategory(cat.id),
        },
        [
          el("div", { class: "tab-icon", html: iconSvg(cat.icon) }),
          el("div", {}, [
            el("p", { class: "tab-label" }, [
              document.createTextNode(cat.label),
            ]),
            el("p", { class: "tab-sublabel" }, [
              document.createTextNode(cat.sublabel),
            ]),
          ]),
          ...(isActive ? [el("div", { class: "tab-indicator" })] : []),
        ],
      );
      return btn;
    }),
  );
}

function levelClass(percentage) {
  if (percentage >= 80) return "full";
  if (percentage >= 45) return "mid";
  return "";
}

function renderStats(container) {
  const data = skillsData[activeCategory];
  const stats = data.top5 ?? [];
  const items = stats.map((s, i) => {
    const node = el(
      "div",
      { class: "stat", style: `animation-delay:${i * 80}ms` },
      [
        el("div", { class: "stat-row" }, [
          el("span", { class: "stat-name" }, [document.createTextNode(s.name)]),
          el("span", { class: `stat-level ${levelClass(s.percentage)}` }, [
            document.createTextNode(s.level),
          ]),
        ]),
        (() => {
          const fill = el("div", { class: "bar-fill" });
          const shine = el("div", { class: "bar-shine" });
          const bar = el("div", { class: "bar" }, [
            el(
              "div",
              { class: "bar-ticks" },
              Array.from({ length: 10 }, () => el("div")),
            ),
            fill,
            shine,
          ]);

          requestAnimationFrame(() => {
            // allow initial paint before animating
            const w = `${s.percentage}%`;
            fill.style.transitionDelay = `${300 + i * 100}ms`;
            shine.style.transitionDelay = `${300 + i * 100}ms`;
            fill.style.width = w;
            shine.style.width = w;
          });
          return bar;
        })(),
      ],
    );
    return node;
  });
  container.replaceChildren(...items);
}

function renderSkills(titleEl, container) {
  const data = skillsData[activeCategory];
  titleEl.textContent = data.title || "Skills";

  const skills = data.skills ?? [];
  const grid = el(
    "div",
    { class: "skills-grid" },
    skills.map((s, i) => {
      const img = el("img", {
        src: s.icon,
        alt: s.name,
        loading: "lazy",
      });
      img.addEventListener("error", () => {
        const holder = img.parentElement;
        if (!holder) return;
        holder.replaceChildren(
          el(
            "span",
            { style: "font-size:13px;font-weight:800;color:#680319" },
            [document.createTextNode((s.name || "?").slice(0, 1))],
          ),
        );
      });

      return el(
        "div",
        {
          class: "skill",
          style: `animation-delay:${i * 50}ms`,
        },
        [
          el("div", { class: "skill-icon" }, [img]),
          el("div", { class: "skill-text" }, [
            el("p", { class: "skill-name" }, [document.createTextNode(s.name)]),
            ...(s.subtitle
              ? [
                  el("p", { class: "skill-subtitle" }, [
                    document.createTextNode(s.subtitle),
                  ]),
                ]
              : []),
          ]),
        ],
      );
    }),
  );

  container.replaceChildren(grid);
}

function setCategory(catId) {
  if (!skillsData[catId]) return;
  activeCategory = catId;

  renderTabs(document.getElementById("tabs-mobile"));
  renderTabs(document.getElementById("tabs-desktop"));

  renderStats(document.getElementById("stats-mobile"));
  renderStats(document.getElementById("stats-desktop"));

  renderSkills(
    document.getElementById("skills-title-mobile"),
    document.getElementById("skills-mobile"),
  );
  renderSkills(
    document.getElementById("skills-title-desktop"),
    document.getElementById("skills-desktop"),
  );
}

